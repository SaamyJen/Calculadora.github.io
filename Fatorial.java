import java.util.Scanner;

public class Fatorial {
  public static void main(String[] args) {
    Scanner sc = new  Scanner(System.in);

    System.out.println("Digite um número natural para calcular o fatorial");
    int número = scanner.nextInt();
    if (número < 0) {
      System.out.println("O fatorial não está definido para números negativos");
      } else {
      int fatorial = 1;
      for  (int i = 1; número ; i++) {
        fatorial = i;

      }
      System.out.println("O fatorial de" + número + fatorial);
      scanner.close();

      
    }

  }
}
 